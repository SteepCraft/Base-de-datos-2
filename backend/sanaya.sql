/

-- 8) Procedimiento de insercion en TERC_PENSUMS calculando periodo
CREATE OR REPLACE PROCEDURE SANAYA.SP_ING_TERC_PENSUMS (
    T_ID IN NUMBER,
    P_ID IN NUMBER
) AS
BEGIN
    INSERT INTO SANAYA.TERC_PENSUMS (
        PENS_ID,
        TERC_ID,
        TEPE_PERIODO
    )
    VALUES (
        P_ID,
        T_ID,
        CASE
            WHEN TO_NUMBER(TO_CHAR(SYSDATE, 'MM')) BETWEEN 1 AND 6
            THEN TO_CHAR(SYSDATE, 'YYYY') || '01'
            ELSE TO_CHAR(TO_NUMBER(TO_CHAR(SYSDATE, 'YYYY')) + 1) || '02'
        END
    );

    COMMIT;
END SP_ING_TERC_PENSUMS;
/

-- 9) Trigger de auditoria de cambios en nota
CREATE OR REPLACE TRIGGER SANAYA.TR_AUDITA_NOTAS
    AFTER UPDATE OF HIST_NOTA ON SANAYA.HISTORIAS
    FOR EACH ROW
BEGIN
    INSERT INTO SANAYA.AUDITORIAS (
        AUDI_USUARIO,
        AUDI_FECHA,
        TERC_ID,
        CURS_ID,
        HIST_NOTA_ANT,
        HIST_NOTA_DESP
    )
    VALUES (
        USER,
        SYSTIMESTAMP,
        :NEW.TERC_ID,
        :NEW.CURS_ID,
        :OLD.HIST_NOTA,
        :NEW.HIST_NOTA
    );
END;
/

-- 10) Vista de auditorias
CREATE OR REPLACE VIEW SANAYA.V_AUDITORIAS AS
SELECT A.AUDI_FECHA AS FECHA,
         A.AUDI_USUARIO AS USUARIO,
         A.TERC_ID,
         T.TERC_NOMBRES,
         T.TERC_APELLIDOS,
         A.CURS_ID,
         B.ASIG_ASIGNATURA,
         A.HIST_NOTA_ANT,
         A.HIST_NOTA_DESP
  FROM SANAYA.AUDITORIAS A
  JOIN SANAYA.TERCEROS T
     ON A.TERC_ID = T.TERC_ID
  JOIN SANAYA.CURSOS C
     ON A.CURS_ID = C.CURS_ID
  JOIN SANAYA.ASIGNATURAS B
     ON C.ASIG_ID = B.ASIG_ID;

-- 11) Funcion de promedio por tercero
CREATE OR REPLACE FUNCTION SANAYA.SP_PROMEDIO (
    P_ID IN NUMBER
) RETURN NUMBER
IS
    V_PROMEDIO NUMBER;
BEGIN
    SELECT ROUND(SUM(H.HIST_NOTA * A.ASIG_CREDITOS) / SUM(A.ASIG_CREDITOS), 2)
      INTO V_PROMEDIO
      FROM SANAYA.HISTORIAS H
      JOIN SANAYA.CURSOS C
         ON H.CURS_ID = C.CURS_ID
      JOIN SANAYA.ASIGNATURAS A
         ON C.ASIG_ID = A.ASIG_ID
     WHERE H.TERC_ID = P_ID;

    RETURN V_PROMEDIO;
END SP_PROMEDIO;
/

-- 12) Vista de promedios
CREATE OR REPLACE VIEW SANAYA.V_PROMEDIOS AS
SELECT TERC_ID AS ID,
         TERC_NOMBRES AS NOMBRES,
         TERC_APELLIDOS AS APELLIDOS,
         SANAYA.SP_PROMEDIO(TERC_ID) AS PROMEDIO
  FROM SANAYA.TERCEROS
 WHERE TERC_TIPO = '0';
