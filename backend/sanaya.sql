CREATE SEQUENCE SANAYA.SQ_ASIGNATURAS
START WITH 54
INCREMENT BY 1
NOCACHE
NOCYCLE;

-- 1) Trigger de acceso por dias y horas laborales
CREATE OR REPLACE TRIGGER SANAYA.ACCESO_DIAS_HORAS
    BEFORE INSERT OR UPDATE OR DELETE ON SANAYA.TERCEROS
DECLARE
    -- Evalua siempre contra la zona horaria de negocio.
    V_NOW TIMESTAMP WITH TIME ZONE := SYSTIMESTAMP AT TIME ZONE 'America/Bogota';
    V_DAY VARCHAR2(3);
    V_HHMM NUMBER(4);
BEGIN
    V_DAY := TO_CHAR(V_NOW, 'DY', 'NLS_DATE_LANGUAGE=ENGLISH');
    V_HHMM := TO_NUMBER(TO_CHAR(V_NOW, 'HH24MI'));

    IF V_DAY IN ('SAT', 'SUN')
        OR V_HHMM < 800
        OR V_HHMM > 1800 THEN
        IF DELETING THEN
            RAISE_APPLICATION_ERROR(-20501, 'Usted solo puede borrar registros en dias y horas laborales');
        ELSIF INSERTING THEN
            RAISE_APPLICATION_ERROR(-20502, 'Usted solo puede insertar registros en dias y horas laborales');
        ELSIF UPDATING THEN
            RAISE_APPLICATION_ERROR(-20503, 'Usted solo puede actualizar registros en dias y horas laborales');
        END IF;
    END IF;
END;
/

-- 2) Procedimiento para insertar ASIGNATURAS
CREATE OR REPLACE PROCEDURE SANAYA.SP_ING_ASIGNATURAS (
    P_ASIGNATURA IN VARCHAR2,
    P_CREDITOS IN NUMBER,
    P_CODIGO IN VARCHAR2
) AS
BEGIN
    INSERT INTO SANAYA.ASIGNATURAS (
        ASIG_ID,
        ASIG_ASIGNATURA,
        ASIG_CREDITOS,
        ASIG_CODIGO
    )
    VALUES (
        SANAYA.SQ_ASIGNATURAS.NEXTVAL,
        P_ASIGNATURA,
        P_CREDITOS,
        P_CODIGO
    );

    COMMIT;
END SP_ING_ASIGNATURAS;
/

-- 3) Procedimiento para actualizar ASIGNATURAS
CREATE OR REPLACE PROCEDURE SANAYA.SP_ACT_ASIGNATURAS (
    P_COD IN NUMBER,
    P_ASIGNATURA IN VARCHAR2,
    P_CREDITOS IN NUMBER,
    P_CODIGO IN VARCHAR2
) AS
BEGIN
    UPDATE SANAYA.ASIGNATURAS
        SET ASIG_ASIGNATURA = P_ASIGNATURA,
             ASIG_CREDITOS = P_CREDITOS,
             ASIG_CODIGO = P_CODIGO
     WHERE ASIG_ID = P_COD;

    COMMIT;
END SP_ACT_ASIGNATURAS;
/

-- 4) Procedimiento para eliminar ASIGNATURAS
CREATE OR REPLACE PROCEDURE SANAYA.SP_DEL_ASIGNATURAS (
    P_COD IN NUMBER
) AS
BEGIN
    DELETE FROM SANAYA.ASIGNATURAS
     WHERE ASIG_ID = P_COD;

    COMMIT;
END SP_DEL_ASIGNATURAS;
/

-- 5) Trigger para crear historias iniciales de primer nivel
CREATE OR REPLACE TRIGGER SANAYA.TR_MAT_PRIMIPAROS
    AFTER INSERT ON SANAYA.TERC_PENSUMS
    FOR EACH ROW
BEGIN
    INSERT INTO SANAYA.HISTORIAS (
        HIST_PERIODO,
        TERC_ID,
        CURS_ID,
        HIST_NOTA
    )
    SELECT CASE
                 WHEN TO_NUMBER(TO_CHAR(SYSDATE, 'MM')) BETWEEN 1 AND 6
                 THEN TO_CHAR(SYSDATE, 'YYYY') || '02'
                 ELSE TO_CHAR(TO_NUMBER(TO_CHAR(SYSDATE, 'YYYY')) + 1) || '01'
             END,
             :NEW.TERC_ID,
             C.CURS_ID,
             NULL
      FROM SANAYA.DETALLE_PENSUMS D
      JOIN SANAYA.CURSOS C
         ON C.ASIG_ID = D.ASIG_ID
     WHERE D.PENS_ID = :NEW.PENS_ID
        AND D.DETAPE_NIVEL = '1';
END;
/

-- 6) Procedimiento de insercion directa en TERC_PENSUMS con periodo manual
